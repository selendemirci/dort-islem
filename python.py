def is_prime(num):
    """Verilen sayının asal olup olmadığını kontrol eder."""
    if num <= 1:
        return False
    for i in range(2, int(num**0.5) + 1):
        if num % i == 0:
            return False
    return True

def nearest_prime_below(n):
    """Girilen sayıdan geriye doğru sayarak en yakın asal sayıyı bulur."""
    for i in range(n, -1, -1):
        if is_prime(i):
            return i
    return None  # Eğer asal sayı bulunamazsa

# Kullanıcıdan giriş al
