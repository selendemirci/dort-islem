import gradio as gr

def hesapla(sayi1, sayi2, islem):
    sayi1 = float(sayi1)
    sayi2 = float(sayi2)

    if islem == "top":
        return sayi1 + sayi2
    elif islem == "çık":
        return sayi1 - sayi2
    elif islem == "böl":
        return sayi1 / sayi2 if sayi2 != 0 else "Sıfıra bölme hatası"
    elif islem == "çarp":
        return sayi1 * sayi2

with gr.Blocks() as app:
    gr.Markdown("## Basit Hesap Makinesi")

    sayi1 = gr.Textbox(label="Birinci Sayı")
    sayi2 = gr.Textbox(label="İkinci Sayı")

    islem = gr.Dropdown(label="İşlem Seçin", choices=["top", "çık", "böl", "çarp"])

    hesapla_btn = gr.Button("Hesapla")
    sonuc = gr.Textbox(label="Sonuç", interactive=False)

    hesapla_btn.click(hesapla, inputs=[sayi1, sayi2, islem], outputs=sonuc)

